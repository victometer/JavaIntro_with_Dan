public class Calculator {

    public int add(int int1, int int2) {
        return int1 + int2;

    }

    public int minus(int i, int i1) {
        return i - i1;
    }

    public int multiply(int i, int i1) {
        return i*i1;
    }


    public double divide(int i, int i1) {
        return i/i1;
    }
}
